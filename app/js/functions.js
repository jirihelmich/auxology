function numerize(string) {
    var number = string;
    if (!number) {
        return NaN;
    }
    if (number.replace) {
        number = number.replace(/[^\d]*(\d+)[,\.]?(\d)?.*/g, "$1$2");
    }
    return parseInt(number);
}

function notify(toaster, title, body, type) {
    type = type || 'info';
    toaster.pop({
        type: type,
        title: title,
        body: body,
        showCloseButton: true,
        timeout: 50000
    });
}
